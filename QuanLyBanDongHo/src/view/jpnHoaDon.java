    public void TKHDBySDT(){
        DefaultTableModel modleHD = (DefaultTableModel)TbHoaDon.getModel();
        if(txtTKHBySDT.getText().isEmpty()){
            loadTbHoaDon();
        }
        else {
            String sdt = txtTKHBySDT.getText();
            for(int i = TbHoaDon.getRowCount()-1;i>=0;i--){
            if(! TbHoaDon.getValueAt(i, 3).toString().equals(sdt)){
                modleHD.removeRow(i);
            }
        }
        }
            
    }

