    public void loadTbHoaDon(){
        HoaDonDAO loadTbHoaDon = new HoaDonDAO();   
        KhachHangDAO khDAO = new KhachHangDAO();
        List<HoaDon> listHoaDon = new ArrayList<>();
        listHoaDon = loadTbHoaDon.getAllHoaDon();
        DefaultTableModel modleSP = (DefaultTableModel)TbHoaDon.getModel();
        modleSP.setRowCount(0);
        for (HoaDon hd : listHoaDon) {
            KhachHang kh = khDAO.getKhachHangByID(hd.getIdKhachHang());
            modleSP.addRow(new Object[]{hd.getIdHoaDon(),
            hd.getIdNhanVien(),
            kh.getTenKhachHang(),
            kh.getSoDienThoai(),
            df.format(hd.getNgayBan()),
            hd.getTongTien(),
            hd.getTienKhachDua(),
            hd.getTienThuaTraKhach()
            });
        }       
    }
    public void loadCTHD (){
        ChiTietHoaDonDAO loadCTHD = new ChiTietHoaDonDAO();
        SanPhamDAO spDAO = new SanPhamDAO();
        int row = TbHoaDon.getSelectedRow();
        if (row != -1) {
            List<ChiTietHoaDon> listCTHD = loadCTHD.getCTHoaDonByID(Integer.parseInt(TbHoaDon.getValueAt(row, 0).toString()));
            DefaultTableModel modleCTHD = (DefaultTableModel)TbChiTietHoaDon.getModel();
            modleCTHD.setRowCount(0);
            for (ChiTietHoaDon cthd : listCTHD) {
                modleCTHD.addRow(new Object[]{cthd.getIdSanPham(),
                spDAO.getSanPhamByID(cthd.getIdSanPham()).getTenSanPham(),
                spDAO.getSanPhamByID(cthd.getIdSanPham()).getGia(),
                cthd.getSoLuong(),
                cthd.getThanhTien()
                });
            }
        }
    }  

